
public class empleados {

    private String txtNombre;
    private String txtTrabajo;
    private String txtMgr;
    private String txtDepartamento;
    
    public empleados() {
    }
    
    

    public String getTxtNombre() {
        return txtNombre;
    }
    public void setTxtNombre(String txtNombre) {
        this.txtNombre = txtNombre;
    }
    public String getTxtTrabajo() {
        return txtTrabajo;
    }
    public void setTxtTrabajo(String txtTrabajo) {
        this.txtTrabajo = txtTrabajo;
    }
    public String getTxtMgr() {
        return txtMgr;
    }
    public void setTxtMgr(String txtMgr) {
        this.txtMgr = txtMgr;
    }
    public String getTxtDepartamento() {
        return txtDepartamento;
    }
    public void setTxtDepartamento(String txtDepartamento) {
        this.txtDepartamento = txtDepartamento;
    }
    
}
